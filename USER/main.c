#include "main.h"

/*-----------------------------------------------------------
 * 硬件初始化函数
 *----------------------------------------------------------*/
static void Hardware_Init(void)
{
	/************************************************************************************************************/
	/*由于共计4个中断，设置中断分组为4，4位抢占优先级，0位亚优先级，因为Free不支持亚优先级处理。
	 由于Free中存在屏蔽优先级阈值的概念，该工程笔者设置为3，因此我们需要调用API函数的中断优先级不能超过3，
	 所以DMA中断优先级为4，定时器4中断优先级为5，TIM2中断优先级为6，从优先级都设置为0--不甘心的咸鱼注*/
	/*************************************************************************************************************/
    NVIC_PriorityGroupConfig(NVIC_PriorityGroup_4);
	
	/* 延时函数初始化 */
    delay_init();
	
	/* 电机PWM初始化
     * TIM1: PWM频率 = 72MHz / 72 / 1000 = 1kHz
     * 死区时间: 100 * 16/72MHz ≈ 22us
     */
    TIM1_dead_pwm_init(1000-1, 72-1, 0, 100);
    motor_init();

    /* 编码器初始化（TIM2）不分频，72MHZ的计数频率,ARR为最大值*/
    TIM2_encode_init(0xFFFF, 0);
	
	/*初始化蜂鸣器*/
	Beep_Init(&bep,GPIOB,GPIO_Pin_15);			 
    
    /* 按键初始化 */
    Key_Init();
    
    /* MQ2气体传感器初始化 */
    MQ2_Init();
	
	/* PID控制器初始化
     * 参数说明：
     * Kp=14.0:   比例系数
     * Ki=1.65:  积分系数
     * Kd=0.00: 微分系数
     * 输出限幅：0~1000
     */
    PID_Init(&g_speedPID, 14.0f, 1.65f, 0.0f, 1000.0f, 0.0f);
	
    /* 风速算法模块初始化 */
    WindSpeed_Init();
    
    /* LCD初始化 */
    LCD_Init();
	
	/* 串口初始化 */
    uart_init(115200);		/*必须放在后边初始化，否则会影响电机转动*/

    /*初始化DMA*/
#if ifopen
	MYDMA_Config(DMA1_Channel5,(u32)&USART1->DR,(u32)receive_buff,buff_size);
#endif  
     
    /* 显示初始化完成 */
    Show_Str(0, 0, BLUE, WHITE, "Init Complete! ", 16, 0);
    delay_ms(500);
}


/*-----------------------------------------------------------
 * 主函数
 *----------------------------------------------------------*/
int main(void)
{
    /* 硬件初始化 */
    Hardware_Init();
    
    /* 系统状态初始化 */
    System_Init();
    
    /* 创建开始任务 */
    StartTask_Create();
    
    /* 启动FreeRTOS调度器 */
    vTaskStartScheduler();
    
    /* 正常情况下不会执行到这里 */
    while(1)
    {
    }
}


